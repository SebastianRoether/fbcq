      SUBROUTINE GRAD(FCT,F0,GRA,VECTOR,NDIM,H,EPS,IZ,NOU)
C
C     ZWECK:
C     BERECHNET DEN GRADIENTEN GRA AN DEM DURCH DIE
C     KOORDINATEN VON VECTOR VORGEGEBENEN PUNKT
C
C     EINGABEPARAMETER:
C     NOU    - AUGABEINHEIT
C     IZ     - FUNKTIONSAUFRUFZAEHLER
C     EPS    - ABBRUCHSCHRANKE FUER GRADIENTENNORM
C     H      - VEKTOR MIT INTERVALLWEITEN FUER GRADIENTENBERECHNUNG
C     NDIM   - DIMENSION DER VEKTOREN
C     VEKTOR - STARTPUNKT FUER GRADIENTENRECHNUNG
C     F0     - FUNKTIONSWERT AM STARTPUNKT
C     FCT    - EXTERNAL MIT FUNKTIONSNAMEN
C
C     AUSGABEPARAMETER:
C     GRA    - GRADIENTENVEKTOR DER DIMENSION NDIM
C     H      - ANGEPASSTER INTERVALLWEITENVEKTOR
C
C     BENOETIGTE UNTERPROGRAMME:
C     FCT
C
C     AUTOR:FRIEDEMANN ROETHER
C     LETZTE AENDERUNG:16.05.87
C
      IMPLICIT REAL*8 (A-H,O-Z)
      DIMENSION GRA(NDIM),VECTOR(NDIM),H(NDIM)
      EXTERNAL FCT
      IF(NOU.GE.2) WRITE(*,'(A34)') '   Numerische Gradientenberechnung'
C
C     BEGINN DER SCHLEIFE ZUR GRADIENTENBERECHNUNG
C
      DO 100 I=1,NDIM
      VEC=VECTOR(I)
C
C     EINSPRUNG, FALLS GRADIENTENRECHNUNG FEHLERHAFT
C
 50   CONTINUE
C
C     NUMERISCHE ZWEI-PUNKT-GRADIENTENRECHNUNG
C
      DIST=H(I)
      IFZ=0
      VECTOR(I)=VEC+DIST
      CALL FCT(VECTOR,WERT1,LI,IZ)           
      IFZ=IFZ+LI
      VECTOR(I)=VEC-DIST
      CALL FCT(VECTOR,WERT2,LI,IZ)            
      IFZ=IFZ+LI
      GRA(I)=(WERT1-WERT2)/(2.D00*DIST)
C
C     UEBERPRUEFUNG AUF QUALITAET UND ANPASSUNG INTERVALLWEITE
C
      IF((WERT1.GT.F0.AND.WERT2.GT.F0).OR.IFZ.GT.0) THEN
         HV=H(I)/10.D00
         IF(HV.GT.0.D00) H(I)=HV
         GO TO 50
      ELSE
         H(I)=H(I)*1.2D00
      END IF
      IF(NOU.GE.2) WRITE(*,'(A9,G14.6,A6,G14.6,A2,G12.4,A5,G12.4)')
     *'   OWert:',WERT1,'UWert:',WERT2,'H:',H(I),'Grad:',GRA(I)
 100  VECTOR(I)=VEC              
      RETURN
      END
