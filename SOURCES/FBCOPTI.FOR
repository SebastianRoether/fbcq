      SUBROUTINE DIF(VECDIF,VEC1,VEC2,EPS,NDIM)
C
C     ZWECK: BERECHNET
C     VECDIF=VEC1-EPS*VEC2
C
C     EINGABE:
C     NDIM  - DIMENSION DER VEKTOREN
C     EPS   - SKALARER MULTIPLIKATOR
C     VEC2  - ZWEITER VEKTOR
C     VEC1  - ERSTER VEKTOR
C
C     AUSGABE:
C     VECDIF- DIFFERENZVEKTOR
C
C     AUTOR:FRIEDEMANN ROETHER
C     LETZTE AENDERUNG:16.05.1987
C
      REAL*8 VECDIF(NDIM),VEC1(NDIM),VEC2(NDIM),EPS
      DO 10 I=1,NDIM
      VECDIF(I)=VEC1(I)-EPS*VEC2(I)
 10   CONTINUE
      RETURN
      END
