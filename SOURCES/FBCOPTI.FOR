      SUBROUTINE RMINKG(FCT,N,D,H,KMAX,VX,F0,IZ,IFAIL,NOU)
C
C
C     ZWECK:
C     MINIMIERUNG MEHRDIMENSIONALER FUNKTIONEN
C     NACH DEM KONJUGIERTE-GRADIENTEN-VERFAHREN
C
C     EINGABEPARAMETER:
C     FCT   - FREI IM ANWENDERPROGRAMM DEFINIERBARE FUNKTION
C     N     - ANZAHL DER UNABH[NGIGEN VARIABLEN
C     D     - REAL-VEKTOR MIT INFORMATION UND ARBEITSPARAMETERN
C             VOM ANWENDER VOR FUNKTIONSAUFRUF FESTZULEGEN
C             D(1): ABBRUCHKRITERIUM F]R GRADIENTENNORM (CA 1.E-7)
C             D(2): ABBRUCHKRITERIUM F]R DIE BERECHNUNG ORTHOGONALER
C                   VEKTOREN (CA 1.E-7...1.E-3)
C             D(3): SCHALTGRENZE FR REGELUNG DES GRADIENTENINTERVALLS
C             D(4): STARTSCHRITTWEITE ZUR EINDIMENSIONALEN MINIMIERUNG
C                   (WIRD DEM ERFOLG SELBSTT[TIG ANGEPASST)
C             D(5): NICHT BELEGEN (INTERNE BERECHNUNG)
C             D(6): ABBRUCHKRITERIUM DER PSEUDO-MINIMIERUNG(CA 0.1..1)
C             D(7): ABBRUCHKRITERIUM DER MINIMIERUNG (CA .01..0.2)
C     H     - VEKTOR MIT STARTWERTEN FR GRADIENTENINTERVALL
C     KMAX  - ANZAHL DER MAXIMAL DURCHZUF]HRENDEN ITERATIONEN
C     VX    - VEKTOR MIT STARTWERTEN, ENTH[LT NACH ABBRUCH IN 
C             VX(1) BIS VX(N) DIE ERMITTELTEN VARIABLEN. IN 
C             VX(N+1) BIS VX(4*N*N) BEFINDEN SICH ARBEITSPARAMETER,
C             DAHER ENTSPRECHENDE DIMENSIONIERUNG ERFORDERLICH.
C
C     AUSGABEPARAMETER:
C     VX    - S.O.
C     F0    - ERMITTELTER MINIMALER FUNKTIONSWERT
C     IZ    - ANZAHL DER AUFRUFE VON FCT
C     IF    - FEHLERMELDUNG:
C             IF=1 - FEHLER IN FUNKTIONSBERECHNUNG
C             IF=2 - ANZAHL DER ITERATIONEN ZUR FUNKTIONSMINIMIERUNG
C                    NICHT AUSREICHEND
C
C     BENOETIGTE UNTERPROGRAMME:
C     FCT   - FREI DEFINIERBARES UNTERPROGRAMM ZUR BERECHNUNG VON F0,
C             DAS MIT CALL FCT(F0,VX,N,P,NP,IF) AUFGERUFEN WIRD. VX
C             DARF W[HREND DER BERECHNUNG NICHT VER[NDERT WERDEN
C     GRAD  - BERECHNET NUMERISCH GRADIENTEN
C     EUKLI - BERECHNET DIE EUKLIDSCHE NORM
C     NORMA - NORMALISIERT EINEN VEKTOR
C     EIN   - EINDIMENSIONALE MINIMIERUNG
C     GRASM - GRAM-SCHMIDTSCHE ORTHOGONALISIERUNG
C     VEC   - VEKTOR-DIFFERENZ
C     XOUT  - BILDSCHIRM-AUSGABE
C
C     AUTOR:FRIEDEMANN ROETHER
C     ERSTE VERSION:22.10.1981
C     LETZTE AENDERUNG:16.05.1987
C
      IMPLICIT REAL*8 (A-H,O-Z)
      DIMENSION VX(*),D(*),H(*)
      EXTERNAL FCT
      IF(N.LE.0) RETURN
C
C     STARTWERTBERECHNUNG
C
      IZ=0
      IF=0
      CALL FCT(VX,F0,LI,IZ)      
      IF(NOU.GE.2) WRITE(*,'(A20,G14.6)') ' Startwert:',F0
      IF(LI.NE.0) GO TO 990
      FMIN=F0
      F00=F0
      K=0
      CALL XOUT(NOU,K,IZ,F00,FMIN,F0,VX,N)
C
C     INDIZIERUNGEN DES ARBEITSVEKTORS VX
C
      LXA=1
      LXE=2*N*(N-1)+1
      LVA=N*(2*N-1)+1
      LVE=2*N*(2*N-1)-N+1
      LGX=LVE+N
C
C     BEGINN DER ITERATION
C
      DO 100 K=1,KMAX
      IF(NOU.GE.2) WRITE(*,'(A8,I3,A20)')
     *' Beginn ',K,' -te Minimierungsgesamtstufe.'
      LXI=LXA
      LVI=LVA
C
C     GRADIENTENVEKTOR BESTIMMEN UND NORMALISIEREN
C     PSEUDO-MINIMIERUNG IN GRADIENTENRICHTUNG
C
      EPS=D(3)
      IF(NOU.GE.2) WRITE(*,'(A31)')
     *' 1. Schritt: Steilster Abstieg.'
      CALL GRAD(FCT,F0,VX(LVA),VX(LXA),N,H,EPS,IZ,NOU)
      GRANOR=EUKLI(VX(LVA),N)
      IF(NOU.GE.2) WRITE(*,'(A20,G14.6)')
     *' Gradientennorm:',GRANOR
      IF(GRANOR.LE.D(1)) GO TO 1020
      CALL NORMA(VX(LVA),N)
      D(5)=D(6)
      FN=F0
      IF(NOU.GE.2) WRITE(*,'(A40)')
     *' Pseudo-Minimierung in Abstiegsrichtung'
      IF(N.GT.1) THEN
         CALL EIN(FCT,F0,VX(LXA+N),VX(LXA),VX(LVA),N,D(3),0,IZ,IF,NOU)
      ELSE
         D(5)=D(7)
         CALL EIN(FCT,F0,VX(3),VX,VX(2),1,D(3),0,IZ,IF,NOU)
         VX(1)=VX(3)
         GO TO 90
      END IF
      IF(N.GT.2) THEN
C
C        BERECHNUNG DER ZU DEN GRADIENTEN ORTHONORMALEN BASISVEKTOREN
C        PSEUDO-MINIMIERUNG IN DEREN RICHTUNG
C
         N1=N-1
         DO 60 I=2,N1
         IF(NOU.GE.2) WRITE(*,'(A30,I3,A30)')
     *' Pseudo-Minimierung in R. des ',I,'ten orthonormalen Gradienten.'
         LXI=LXI+N
         LVI=LVI+N
         CALL GRAD(FCT,F0,VX(LGX),VX(LXI),N,H,EPS,IZ,NOU)
         IF(EUKLI(VX(LGX),N).LE.D(1)) GO TO 1010
         CALL GRASM(VX(LVI),VX(LGX),VX(LVA),I,N)
         ORTNOR=EUKLI(VX(LVI),N)
         IF(NOU.GE.2) WRITE(*,'(A16,G14.6)')
     *   ' Gradientennorm:',ORTNOR
         IF(ORTNOR.LE.D(2)) THEN
C
C        WENN PSEUDO-MINIMIERUNG NICHT AUSREICHT, MINIMIERUNG ZUR BE-
C        STIMMUNG DER ORTHONORMALEN BASISVEKTOREN
C
            IF(NOU.GE.2) THEN
               WRITE(*,'(A50)') ' Pseudo-Minimierung reicht nicht aus.'
               WRITE(*,'(A30,I3,A30)')
     *   ' Minimierung in Richt. des ',I,'ten orthonormalen Gradienten.'
            END IF
            D(5)=D(7)
         CALL EIN(FCT,FN,VX(LXI),VX(LXI-N),VX(LVI-N),N,D(3),0,IZ,IF,NOU)
            F0=FN
            CALL GRAD(FCT,F0,VX(LGX),VX(LXI),N,H,EPS,IZ,NOU)
            IF(EUKLI(VX(LGX),N).LE.D(1)) GO TO 1010
            CALL GRASM(VX(LVI),VX(LGX),VX(LVA),I,N)
            ORTNOR=EUKLI(VX(LVI),N)
            IF(NOU.GE.2) WRITE(*,'(A16,G14.6)')
     *      ' Gradientennorm:',ORTNOR
            IF(ORTNOR.LE.D(2)) THEN
C
C           GRADIENT HAT SICH BEI NEUBERECHNUNG NICHT VER[NDERT. DAHER
C           VORGEZOGENEN EINDIMENSIONALE MINIMIERUNGEN AUF DEN UNTER-
C           EBENEN DES N-DIMENSIONALEN VEKTORRAUMS ZUR BESTIMMUNG DER
C           GNSTIGSTEN TASTRICHTUNG
C
               IF(NOU.GE.2)
     *         WRITE(*,'(A32)')' Auch die Minimierung erfolglos.'
               IF(I.GT.2) THEN
                  LXS=LXI+N
                  I1=I-1
                  DO 30 J=2,I1
                     IF(NOU.GE.2) WRITE(*,'(A30,I3,A11)')
     *               ' Vorgezogene Minimierung auf ',J,' ter Ebene.'
                     LXI=LXI+N
                     LVI=LVI+N
                     CALL DIF(VX(LVI-N),VX(LXI-N),VX(LXS-J*N),1.D00,N)
                     CALL NORMA(VX(LVI-N),N)
                     D(5)=D(7)
         CALL EIN(FCT,F0,VX(LXI),VX(LXI-N),VX(LVI-N),N,D(3),1,IZ,IF,NOU)
 30               CONTINUE
               END IF
               CALL DIF(VX(LVE),VX(LXI),VX(LXA),1.D00,N)
               CALL DIF(VX(LXE),VX(LXI),VX(LXA),0.D00,N)
               GO TO 85
            END IF
         END IF
         CALL NORMA(VX(LVI),N)
         D(5)=D(6)
         FN=F0
         CALL EIN(FCT,F0,VX(LXI+N),VX(LXI),VX(LVI),N,D(3),1,IZ,IF,NOU)
 60      CONTINUE
C
C     BESTIMMUNG EINES NEUEN ZUFLLIGEN BASISVEKTORS UND EINDIMENS-
C     IONALE MINIMIERUNG IN DESSEN RICHTUNG
C
      END IF
      LXI=LXI+N
      LVI=LVI+N
      IF(NOU.GE.2) WRITE(*,'(A40)') ' Minimierung in bel. orth.Richt.'
      DO 75 I=1,N
         VX(LGX-1+I)=1.
 75   CONTINUE
      CALL NORMA(VX(LGX),N)
      CALL GRASM(VX(LVI),VX(LGX),VX(LVA),N,N)
      CALL NORMA(VX(LVI),N)
      D(5)=D(7)
      CALL EIN(FCT,F0,VX(LXI+N),VX(LXI),VX(LVI),N,D(3),1,IZ,IF,NOU)
      IF(N.GT.2) THEN
C
C     EINDIMENSIONALE MINIMIERUNGEN AUF DEN UNTEREBENEN DES N-DIMENS-
C     IONALEN VEKTORRAUMES ZUR BESTIMMUNG DER GUENSTIGSTEN TASTRICHT.
C
         N1=N+1
         N2=2*N-2
         IF(NOU.GE.2) WRITE(*,'(A60)')
     *   ' Minimierung auf den Unterebenen der Haupttastrichtung.'
         DO 80 I=N1,N2
            LXI=LXI+N
            LVI=LVI+N
            CALL DIF(VX(LVI),VX(LXI),VX(N*(2*N-1-I)+1),1.D00,N)
            CALL NORMA(VX(LVI),N)
            D(5)=D(7)
           CALL EIN(FCT,F0,VX(LXI+N),VX(LXI),VX(LVI),N,D(3),1,IZ,IF,NOU)
 80      CONTINUE
      END IF
C
C     SCHRITT IN ERMITTELTE TASTRICHTUNG
C
      CALL DIF(VX(LVE),VX(LXE),VX(LXA),1.D00,N)
 85   CONTINUE
      CALL NORMA(VX(LVE),N)
      IF(NOU.GE.2) WRITE(*,'(A40)')
     *' Schritt in die ermittelte Tastrichtung.'
      D(5)=D(7)
      CALL EIN(FCT,F0,VX(LXA),VX(LXE),VX(LVE),N,D(3),1,IZ,IF,NOU)
C
C     ABBRUCH, FALLS KEINE VERBESSERUNG MEHR MGLICH
C
 90   CONTINUE
      CALL XOUT(NOU,K,IZ,F00,FMIN,F0,VX,N)
      IF(SNGL(F0).GE.SNGL(FMIN)) GO TO 1000
      FMIN=F0
 100  CONTINUE
C
C     MINIMIERUNG MIT VORGESEHENER SCHRITTZAHL NICHT ABGESCHLOSSEN
C
      IFAIL=2
      GO TO 1100
C
C     STARTWERTE NICHT ZULAESSIG
C
 990  CONTINUE
      IFAIL=1
      GO TO 1100
C
C     MINIMIERUNG OKAY
C
 1000 CONTINUE
      IFAIL=0
      GO TO 1100
C
C     GRADIENTENNORM UNTERSCHRITTEN
C
 1010 CONTINUE
      CALL DIF(VX(LXA),VX(LXI),VX(LVA),0.D00,N)
 1020 CONTINUE
      IFAIL=3
 1100 CONTINUE
      RETURN
      END
