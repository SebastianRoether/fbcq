      SUBROUTINE GRASM(VECNEW,VECOLD,FELD,INDEX,NDIM)
C
C     ZWECK:
C     BERECHNET DEN ORTHOGONALEN BASISVEKTOR VECNEW AUS
C     VECOLD UND DEM FELD MIT DEN VORHER BERECHNETEN BASIS-
C     VEKTOREN NACH DER METHODE VON GRAM/SCHMIDT
C
C     EINGABE:
C     NDIM  - DIMENSION DER VEKTOREN VECNEW UND VECOLD
C     INDEX - ZEIGER, DER DIE ANZAHL BISHERIGER VEKTOREN REPRESENTIERT
C     FELD  - FELD MIT BISHERIGEN VEKTOREN DER DIMENSION NDIM*INDEX
C     VECOLD- AUSGANGSVEKTOR
C
C     AUSGABE:
C     VECNEW- ORTHONORMALER BASISVEKTOR
C
C     AUTOR:FRIEDEMANN ROETHER
C     LETZTE AENDERUNG:16.05.1987
C
      REAL*8 VECNEW(NDIM),VECOLD(NDIM),FELD(*),PROD
      DO 10 I=1,NDIM
 10   VECNEW(I)=VECOLD(I)
      IF(INDEX.LE.1) RETURN
      IND1=INDEX-1
      DO 20 I=1,IND1
      PROD=0.
      DO 30 J=1,NDIM
 30   PROD=PROD+VECOLD(J)*FELD(NDIM*(I-1)+J)
      DO 40 J=1,NDIM
 40   VECNEW(J)=VECNEW(J)-PROD*FELD(NDIM*(I-1)+J)
 20   CONTINUE
      RETURN
      END
