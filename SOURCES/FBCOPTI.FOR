c
      SUBROUTINE NORMA(VEC,NDIM)
C
C     ZWECK:NORMALISIERUNG DES VEKTORS VEC
C
C     EINGABE:
C     NDIM  - DIMENSION DES VEKTORS VEC
C     VEC   - ZU NORMALISIERENDER VEKTOR
C
C     AUSGABE:
C     VEC   - NORMALISIERTER VEKTOR
C
C     AUTOR:FRIEDEMANN ROETHER
C     LETZTE AENDERUNG:16.05.1987
C
C     BENOETIGTE UNTERPROGRAMME:
C     EUKLI
C
      REAL*8 VEC(NDIM),SUM,EUKLI
      SUM=EUKLI(VEC,NDIM)
      IF(SUM.EQ.0.D00) THEN
         VEC(NDIM)=1.D00
      ELSE
         DO 10 I=1,NDIM
            VEC(I)=VEC(I)/SUM
 10      CONTINUE
      END IF
      RETURN
      END
