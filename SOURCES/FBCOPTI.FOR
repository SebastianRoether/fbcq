      SUBROUTINE EIN(FCT,F0,X,X0,GX,NDIM,EPS,IGRAD,IZ,IFAIL,NOU)
C
C     ZWECK: SUCHE X=X0+EPS*GX, WOBEI F(X)=MINIMUM
C     EINDIMENSIONALE MINIMIERUNG IN RICHTUNG DES VEKTORS GX
C     NACH DER METHODE DES GOLDENEN SCHNITTS MIT ABSCHLIESSENDER
C     INTERPOLATION DURCH QUADRATISCHES POLYNOM
C
C
C     EINGABEPARAMETER:
C     NOU   - AUSGABEEINHEIT FUER ZWISCHENWERTE
C     IFAIL - VERSCHIEDEN VON NULL, FALLS KEINE MINIMIERUNG MOEGLICH
C     IZ    - FUNKTIONSAUFRUFZAEHLER VON FCT
C     IGRAD - 0:RICHTUNGSBERECHNUNG,1:KEINE RICHTUNGSBERECHNUNG
C     EPS   - STARTSCHRITTWEITE FUER MINIMUMSUCHE
C     NDIM  - DIMENSION DER VEKTOREN X,X0 UND GX
C     GX    - GRADIENTENRICHTUNGSVEKTOR
C     X0    - STARTPUNKT
C     X     - AKTUELLER SUCHPUNKT
C     F0    - FUNKTIONSWERT AM STARTPUNKT F0=F(X0)
C     FCT   - EXTERNAL MIT FUNKTIONSNAMEN
C
C     AUSGABEPARAMETER:
C     X     - NEUER PUNKT MIT GEWUENSCHTER EIGENSCHAFT
C     EPS   - AKTUALISIERTE SCHRITTWEITE
C
C
C     BENOETIGTE UNTERPROGRAMME:
C     DIF   - DIFFERENZ ZWEIER VEKTOREN
C     FCT   - GUETEFUNKTIONSAUFRUF
C
C     AUTOR: FRIEDEMANN ROETHER
C     ERSTE VERSION:12.12.1981
C     LETZTE AENDERUNG:16.05.1987
C
      IMPLICIT REAL*8 (A-H,O-Z)
      DIMENSION X(NDIM),X0(NDIM),GX(NDIM),EPS(*)
      EXTERNAL FCT
      DATA GS/1.618034D00/,IZMAX/10/
      IFAIL=0
      HS=DABS(EPS(2))
      F2=F0
      E2=0.
      IF(NOU.GE.2) WRITE(*,'(A30)') '   Eindimensionale Minimierung'
C
C     RICHTUNG SUCHEN, FALLS IGRAD=1
C
      IF(IGRAD.NE.0) THEN
         IF(NOU.GE.2) WRITE(*,'(A30)') '   Minimierungsrichtung suchen'
         E1=EPS(2)/10.
         ISEMA=0
 10      CONTINUE
         CALL DIF(X,X0,GX,E1,NDIM)
         CALL FCT(X,F1,LI,IZ)
         IF(F1.EQ.F0.AND.LI.EQ.0) THEN
C
C           TESTINTERVALL IST ZU KLEIN
C
            E1=E1*GS
            ISEMA=1
            GO TO 10
         END IF
         IF(F1.GT.F0.OR.LI.NE.0) THEN
            E1=-E1
            CALL DIF(X,X0,GX,E1,NDIM)
            CALL FCT(X,F1,LI,IZ)
            IF(LI.NE.0.OR.F1.GT.F0) THEN
C
C           TESTINTERVALL IST ZU GROSS
C
               E1=-E1/10.
               IF(ISEMA) 500,10,500
            END IF
C
C           RICHTUNG IST NEGATIV
C
            HS=-HS
            IF(NOU.GE.2) WRITE(*,'(A20)') '   Richtung negativ.'
         ELSE
            IF(NOU.GE.2) WRITE(*,'(A20)') '   Richtung positiv.'
         END IF
      IF(ABS(HS).GT.ABS(E1)) HS=E1
      END IF
C
C     BERECHNUNG DES WERTES IM ABSTAND H0
C
      EX=HS
      CALL DIF(X,X0,GX,EX,NDIM)
      CALL FCT(X,FX,LI1,IZ)         
      IF(LI1.NE.0.OR.FX.GE.F2) GO TO 30
C
C     SCHRITTWEITE VERGROESSERN
C
      E1=EX
      F1=FX
      IZE=IZ
      EX=EX*(1.D00+GS)
 20   CALL DIF(X,X0,GX,EX,NDIM)
      CALL FCT(X,FX,LI,IZ)            
      IF(NOU.GE.2) WRITE(*,'(A27,G14.6)')
     *'   Schrittweite vergrÃern:',FX
      IF(LI.EQ.0) GO TO 25
      E2=E1
      F2=F1
      GO TO 200
 25   CONTINUE
      E3=E2
      E2=E1
      E1=EX
      F3=F2
      F2=F1
      F1=FX
      EX=EX+(EX-E2)*GS
      IF(IZ-IZE.GE.2*IZMAX) GO TO 200
      IF(FX.LE.F2) GO TO 20
      GO TO 40
C
C     SCHRITTWEITE REDUZIEREN
C
 30   E3=0.
      E2=EX
      F3=F0
      F2=FX
      IZE=IZ
 35   EX=EX/(1.D00+GS)
      CALL DIF(X,X0,GX,EX,NDIM)
      CALL FCT(X,FX,LI,IZ)             
      IF(NOU.GE.2) WRITE(*,'(A27,G14.6)')
     *'   Schrittweite reduzieren:',FX
      IF(IZ-IZE.GE.IZMAX) GO TO 400
      IF(LI.NE.0) GO TO 35
      IF(LI1.NE.0) THEN
         LI1=0
         GO TO 30
      END IF
      E1=E2
      E2=EX
      F1=F2
      F2=FX
      IF(FX.GT.F1) GO TO 400
      IF(FX.GT.F3) GO TO 35
C
C     VERKLEINERN DES UNSICHERHEITSINTERVALLES
C
 40   IGT=3
      ILT=1
      FMIN=F2
      IZE=IZ
      AINTEV=DABS(E1-E3)
 50   IF(F0.EQ.F2) GO TO 100
      ABSTAN=DABS(E1-E3)
      IF(ABSTAN.EQ.0.) GO TO 500
      IF(ABSTAN/AINTEV.LE.EPS(3)) GO TO 100
      EX=E1-E2+E3
      IF(DABS(EX).LE.DABS(E3).OR.DABS(EX).GE.DABS(E1)) GO TO 200
      CALL DIF(X,X0,GX,EX,NDIM)
      CALL FCT(X,FX,LI,IZ)           
      IF(NOU.GE.2) WRITE(*,'(A37,G14.6)')
     *'   Unsicherheitsintervall reduzieren:',FX
      IF(FX.GE.F1.AND.FX.GE.F3.OR.LI.NE.0) GO TO 200
      IF(IZ-IZE.GE.IZMAX) GO TO 200
      IF(FX.GT.FMIN) GO TO(60,70,80,90),IGT
      FMIN=FX
      GO TO (60,70,80,90),ILT
 60   E3=E2
      E2=EX
      F3=F2
      F2=FX
 65   ILT=1
      IGT=3
      GO TO 50
 70   E1=E2
      E2=EX
      F1=F2
      F2=FX
 75   ILT=2
      IGT=4
      GO TO 50
 80   E1=EX
      F1=FX
      GO TO 75
 90   E3=EX
      F3=FX
      GO TO 65
 100  CONTINUE
      IF(F0.LT.F2) GO TO 400
C
C     INTERPOLATION DURCH QUADRATISCHES POLYNOM
C
      A=F3*(E2-E1)-F2*(E3-E1)+F1*(E3-E2)
      B=(E2-E1)*(E2-E1)*(F3-F1)
      B=B-(E3-E1)*(E3-E1)*(F2-F1)
      IF(A.EQ.0.D00) GO TO 200
      EX=E1+B/(2.0D0*A)
      IF(DABS(EX).GT.DABS(E1)+DABS(E3)) GO TO 200
      CALL DIF(X,X0,GX,EX,NDIM)
      CALL FCT(X,FX,LI,IZ)           
      IF(NOU.GE.2) WRITE(*,'(A27,G14.6)')
     *'   Interpolationspolynom:',FX
      IF(FX.GT.F2.OR.LI.NE.0) GO TO 200
      F0=FX
      E2=EX
      GO TO 300
 200  CALL DIF(X,X0,GX,E2,NDIM)
      F0=F2
C
C     NEUBERECHNUNG DER STARTSCHRITTWEITE
C
 300  EPS(2)=0.5*EPS(2)+0.5*DABS(E2)
      RETURN
 400  IFAIL=2
 500  CONTINUE
      IF(NOU.GE.2) WRITE(*,'(A30)') '   Absoluter Fehlschlag.'
      CALL DIF(X,X0,GX,0.D00,NDIM)
      RETURN
      END
