      REAL*8 FUNCTION EUKLI(VEC,NDIM)
C
C     ZWECK:
C     BERECHNET DIE EUKLIDSCHE NORM DES VEKTORS VEC
C
C     EINGABEPARAMETER:
C     NDIM  - DIMENSION DES VEKTORS VEC
C     VEC   - EINGABEVEKTOR
C
C     AUSGABEPARAMETER:
C     EUKLI - EUKLIDSCHE NORM DES VEKTORS VEC
C
C     AUTOR:FRIEDEMANN ROETHER
C     LETZTE AENDERUNG:16.05.1987
C
      REAL*8 VEC(NDIM)
      EUKLI=0.D00
      DO 10 I=1,NDIM
 10   EUKLI=EUKLI+VEC(I)*VEC(I)
      EUKLI=DSQRT(EUKLI)
      RETURN
      END
